import type { RmemEncodingName } from "../contracts/encoding.js";

export const SINGLE_BYTE_ENCODING_NAMES = Object.freeze([
  "windows-1251",
  "windows-1252",
  "iso-8859-1",
  "iso-8859-5",
  "koi8-r",
  "cp866",
] as const satisfies readonly RmemEncodingName[]);

export type SingleByteEncodingName = (typeof SINGLE_BYTE_ENCODING_NAMES)[number];

const UNMAPPED_CODE_POINT = -1;

export function isSingleByteEncoding(value: unknown): value is SingleByteEncodingName {
  return (
    typeof value === "string" &&
    SINGLE_BYTE_ENCODING_NAMES.includes(value as SingleByteEncodingName)
  );
}

export function decodeSingleByteCodePoint(
  byte: number,
  encoding: SingleByteEncodingName,
): number | undefined {
  assertByteValue(byte);

  if (byte < 0x80) {
    return byte;
  }

  const codePoint = singleByteHighByteCodePointTable(encoding)[byte - 0x80];

  return codePoint === undefined || codePoint === UNMAPPED_CODE_POINT ? undefined : codePoint;
}

export function encodeSingleByteCodePoint(
  codePoint: number,
  encoding: SingleByteEncodingName,
): number | undefined {
  assertCodePointValue(codePoint);

  if (codePoint <= 0x7f) {
    return codePoint;
  }

  if (codePoint >= 0xd800 && codePoint <= 0xdfff) {
    return undefined;
  }

  return singleByteCodePointToByteTable(encoding).get(codePoint);
}

export function singleByteHighByteCodePointTable(
  encoding: SingleByteEncodingName,
): readonly number[] {
  switch (encoding) {
    case "windows-1251":
      return WINDOWS_1251_HIGH_BYTE_CODE_POINTS;
    case "windows-1252":
      return WINDOWS_1252_HIGH_BYTE_CODE_POINTS;
    case "iso-8859-1":
      return ISO_8859_1_HIGH_BYTE_CODE_POINTS;
    case "iso-8859-5":
      return ISO_8859_5_HIGH_BYTE_CODE_POINTS;
    case "koi8-r":
      return KOI8_R_HIGH_BYTE_CODE_POINTS;
    case "cp866":
      return CP866_HIGH_BYTE_CODE_POINTS;
    default:
      return unsupportedSingleByteEncoding(encoding);
  }
}

function unsupportedSingleByteEncoding(encoding: never): never {
  throw new RangeError(`Unsupported single-byte encoding: ${String(encoding)}.`);
}

function createLinearHighByteCodePoints(start: number): readonly number[] {
  return Object.freeze(Array.from({ length: 0x80 }, (_, index) => start + index));
}

function assertByteValue(byte: number): void {
  if (!Number.isInteger(byte) || byte < 0 || byte > 0xff) {
    throw new RangeError("Single-byte value must be an integer from 0 to 255.");
  }
}

function assertCodePointValue(codePoint: number): void {
  if (!Number.isInteger(codePoint) || codePoint < 0 || codePoint > 0x10ffff) {
    throw new RangeError("Code point must be an integer from 0 to 0x10FFFF.");
  }
}

function singleByteCodePointToByteTable(
  encoding: SingleByteEncodingName,
): ReadonlyMap<number, number> {
  switch (encoding) {
    case "windows-1251":
      return WINDOWS_1251_CODE_POINT_TO_BYTE;
    case "windows-1252":
      return WINDOWS_1252_CODE_POINT_TO_BYTE;
    case "iso-8859-1":
      return ISO_8859_1_CODE_POINT_TO_BYTE;
    case "iso-8859-5":
      return ISO_8859_5_CODE_POINT_TO_BYTE;
    case "koi8-r":
      return KOI8_R_CODE_POINT_TO_BYTE;
    case "cp866":
      return CP866_CODE_POINT_TO_BYTE;
    default:
      return unsupportedSingleByteEncoding(encoding);
  }
}

function createSingleByteCodePointToByteTable(
  highByteCodePoints: readonly number[],
): ReadonlyMap<number, number> {
  const entries = new Map<number, number>();

  for (let index = 0; index < highByteCodePoints.length; index += 1) {
    const codePoint = highByteCodePoints[index];

    if (codePoint === undefined || codePoint === UNMAPPED_CODE_POINT || entries.has(codePoint)) {
      continue;
    }

    entries.set(codePoint, index + 0x80);
  }

  return entries;
}

